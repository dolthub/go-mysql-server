package plan

import (
	"github.com/dolthub/go-mysql-server/sql"
)

// NewCachedResults returns a cached results plan Node, which will cache results generated by Child.RowIter() and return
// those results for future calls to RowIter. This node is only safe to use if the Child is deterministic and is not
// dependent on the |row| parameter in the call to RowIter.
func NewCachedResults(n sql.Node) *CachedResults {
	return &CachedResults{
		UnaryNode: UnaryNode{n},
	}
}

// CachedResults stores the results of the child node's iterator for faster subsequent retrieval.
type CachedResults struct {
	UnaryNode
	cachedResults []sql.Row
	// finalized is set when the child node's RowIter has been exhausted so subsequent RowIters will be read from the
	// cached result rather than reading from the child node again.
	finalized bool
}

var _ sql.Node = (*CachedResults)(nil)
var _ sql.CollationCoercible = (*CachedResults)(nil)

func (n *CachedResults) String() string {
	pr := sql.NewTreePrinter()
	_ = pr.WriteNode("CachedResults")
	_ = pr.WriteChildren(n.UnaryNode.Child.String())
	return pr.String()
}

func (n *CachedResults) DebugString(ctx *sql.Context) string {
	pr := sql.NewTreePrinter()
	_ = pr.WriteNode("CachedResults")
	_ = pr.WriteChildren(sql.DebugString(ctx, n.UnaryNode.Child))
	return pr.String()
}

func (n *CachedResults) WithChildren(ctx *sql.Context, children ...sql.Node) (sql.Node, error) {
	if len(children) != 1 {
		return nil, sql.ErrInvalidChildrenNumber.New(n, len(children), 1)
	}
	nn := *n
	nn.UnaryNode.Child = children[0]
	return &nn, nil
}

// CollationCoercibility implements the interface sql.CollationCoercible.
func (n *CachedResults) CollationCoercibility(ctx *sql.Context) (collation sql.CollationID, coercibility byte) {
	return sql.GetCoercibility(ctx, n.Child)
}

func (n *CachedResults) SetCachedResults(rows []sql.Row) {
	n.cachedResults = rows
	n.finalized = true
}

func (n *CachedResults) IsFinalized() bool {
	return n.finalized
}

func (n *CachedResults) GetCachedResults() []sql.Row {
	return n.cachedResults
}

func (n *CachedResults) IsReadOnly() bool {
	return n.Child.IsReadOnly()
}
